    public int getDias() {

        Date past = dtpFechaInicio.getDate(); 
        Date today = dtpFechaFinal.getDate(); 
        int days = Days.daysBetween(new DateTime(past), new DateTime(today)).getDays();
        
        return days;
    }
    
